import React from 'react';
import ReactDOM from 'react-dom';
import './index.css';

// React component
// Returns a view of Square Component taking in a param named value, from Board Component
class Square extends React.Component {
  constructor(props) {
	  super(props);
	  this.state = {
			  value: null,
	  };
  }	
	
  render() {
    return (
      <button className="square" onClick={() => this.setState({value: 'X'})}>
        {this.state.value}
      </button>
    );
  }
}

// React component
// The renderSquare(i) returns a descr. of component Square, which sets value={i}
// In the Square class, in the render method, it returns with {this.props.value},
// where props is the param it takes in, and value is the name of the param from Board
class Board extends React.Component {
  renderSquare(i) {
    return <Square value={i}/>;
  }

  render() {
    const status = 'Next player: X';

    return (
      <div>
        <div className="status">{status}</div>
        <div className="board-row">
          {this.renderSquare(0)}
          {this.renderSquare(1)}
          {this.renderSquare(2)}
        </div>
        <div className="board-row">
          {this.renderSquare(3)}
          {this.renderSquare(4)}
          {this.renderSquare(5)}
        </div>
        <div className="board-row">
          {this.renderSquare(6)}
          {this.renderSquare(7)}
          {this.renderSquare(8)}
        </div>
      </div>
    );
  }
}

// React component
class Game extends React.Component {
  render() {
    return (
      <div className="game">
        <div className="game-board">
          <Board />
        </div>
        <div className="game-info">
          <div>{/* status */}</div>
          <ol>{/* TODO */}</ol>
        </div>
      </div>
    );
  }
}

